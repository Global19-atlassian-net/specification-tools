package org.eclipse.jakartaee.tools.bps;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.Singular;

import java.util.List;

/**
 * Very light config to cut down on commenting code in/out
 * and making it easier to inch towards success.
 */
@Data
@AllArgsConstructor
@NoArgsConstructor
@Builder
public class Config {

    public boolean createFork;
    public boolean pushFork;
    public boolean createPrs;
    public boolean deleteFork;
    public boolean deleteLocal;
    @Singular
    public List<String> branches;
}
